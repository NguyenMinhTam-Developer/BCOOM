      length: JsonMapperUtils.safeParseInt(json['length']),
      width: JsonMapperUtils.safeParseInt(json['width']),
      height: JsonMapperUtils.safeParseInt(json['height']),
      status: JsonMapperUtils.safeParseInt(json['status']),
